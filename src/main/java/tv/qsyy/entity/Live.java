public class Live {

    private String liveUserId;
    private String liveNickname;
    private String liveScreenShot;

    public String getLiveUserId() {
        return liveUserId;
    }

    public void setLiveUserId(String liveUserId) {
        this.liveUserId = liveUserId;
    }

    public String getLiveNickname() {
        return liveNickname;
    }

    public void setLiveNickname(String liveNickname) {
        this.liveNickname = liveNickname;
    }

    public String getLiveScreenShot() {
        return liveScreenShot;
    }

    public void setLiveScreenShot(String liveScreenShot) {
        this.liveScreenShot = liveScreenShot;
    }
}
